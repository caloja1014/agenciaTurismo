public class Ciudad {
    private String idCiudad;
    private String idProvincia;
    private String nombre;

    /**
     * @return the idCiudad
     */
    public String getIdCiudad() {
        return idCiudad;
    }

    /**
     * @return the idProvincia
     */
    public String getIdProvincia() {
        return idProvincia;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }
    
    
}
